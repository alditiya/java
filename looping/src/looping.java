public class looping {
    public static void main(String args[]) {
// For Loop akan mengeluarkan output print: 1-10
    for(int i = 1; i<=10; i++){
    System.out.println("Output Berurut: " + i );
    }

    // Ini akan mengeluarkan output print: 10 - 1
    for(int i = 10; i>=1; i--){
    System.out.println("Output Terbalik: " + i );
    }
    }
}
   